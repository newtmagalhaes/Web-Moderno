// Operador de Atribuição em JavaScript

/**
 * A atribuição em JavaScript
 * pode ser por valor (cópia)
 * ou por referência, dependendo
 * do dado à direita do operador
 */

let var1 = 42; // Atribuição: atribui o dado à direita à variável à esquerda;
console.log("var1 é:", var1);

var1 += 24; // Atribuição aditiva: equivalente a var1 = var1 + 24;
console.log("var1 += 24 é:", var1);

var1 -= 24; // Atribuição subtrativa: equivalente a var1 = var1 - 24;
console.log("var1 -= 24 é:", var1);

var1 *= 2; // Atribuição multiplicativa: equivalente a var1 = var1 * 2;
console.log("var1 *= 2 é:", var1);

var1 /= 2; // Atribuição divisiva: equivalente a var1 = var1 / 24;
console.log("var1 /= 2 é:", var1);

var1 %= 4; // Atribuição modular: equivalente a var1 = var1 % 4;
console.log("var1 %= 4 é:", var1);
