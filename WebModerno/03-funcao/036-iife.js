// IIFE em JavaScript

/**
 * IIFE: Immediately Invoked Function Expression
 * Funções chamadas logo após a sua declaração
 * É útil para fugir do escopo global no Browser
 */

(function () {
  console.log("É preciso declarar a função entre parêntesis");
  console.log("por exemplo: (function name (param) {code})()");
})();
